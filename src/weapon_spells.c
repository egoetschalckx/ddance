void weapon_spell( CHAR_DATA *ch, OBJ_DATA *weapon, int spell)
{
	CHAR_DATA *vch;
	CHAR_DATA *vch_next;
	for ( vch = ch->in_room->people; vch; vch = vch_next )
	{
	    vch_next	= vch->next_in_room;

	    switch ( skill_table[weapon->pIndexData->weapon_spell].target )
	    {
	    default:
		bug( "weapon_spell: bad target for sn %d.",weapon->pIndexData->weapon_spell );
		return;

	    case TAR_IGNORE:
		if ( vch != ch )
		    continue;
		break;

	    case TAR_CHAR_OFFENSIVE:
		if ( IS_NPC(ch) ? IS_NPC(vch) : !IS_NPC(vch) )
		    continue;
		break;
		
	    case TAR_CHAR_DEFENSIVE:
		if ( IS_NPC(ch) ? !IS_NPC(vch) : IS_NPC(vch) )
		    continue;
		break;

	    case TAR_CHAR_SELF:
		if ( vch != ch )
		    continue;
		break;

		case TAR_OBJ_INV:
			return;
	    }

	    act( "{W$n{W's $p {Wis suddenly suffused with mana.{x", ch, weapon, NULL, TO_ROOM );
	    act( "$p {Wis suddenly suffused with mana.{x", ch, weapon, NULL, TO_CHAR );
	    obj_cast_spell( weapon->pIndexData->weapon_spell,ch->level, ch, vch, NULL );
	}
}